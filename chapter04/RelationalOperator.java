public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        double d = 10;
        System.out.println(a == d); // true

        char c = 97;
        System.out.println(c);
        System.out.println(c == 97); // true

        short s = 97;
        System.out.println(c == s); // true

        
    }
}