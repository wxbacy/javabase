public class IntDetail {
    public static void main(String[] args) {
        byte a = 1;
        short b = 1;
        int c = 1;
        long d = 1;
        System.out.println("a:" + a);
        System.out.println("b:" + b);
        System.out.println("c:" + c);
        System.out.println("d:" + d);

        int num1 = 2;
        double num2 = 2;
        // 不相等
        if (num1 == num2) {
            System.out.println("equal num1 num2");
        }
    }
}